package com.gedharizka.javafundamental.Operator;

public class OperatorAritmatika {
    public static void main(String[] args) {
        System.out.println("Operasi Penjumlahan");
        int hasilPenjumlahan = 5+5 ;
        System.out.println("Hasil 5+5 = " + hasilPenjumlahan);
        System.out.println();
        System.out.println("=================================");

        System.out.println();
        System.out.println("Operasi Pengurangan");
        int hasilPengurangan = 9-3;
        System.out.println("Hasil 9-3 = " + hasilPengurangan);
        System.out.println();
        System.out.println("=================================");

        System.out.println();
        System.out.println("Operasi Perkalian");
        int hasilPerkalian = 5*5;
        System.out.println("Hasil 5*5 = " + hasilPerkalian);
        System.out.println();
        System.out.println("=================================");

        System.out.println();
        System.out.println("Operasi Pembagian");
        int hasilPembagian = 9/3;
        System.out.println("Hasil 9 : 3 = " + hasilPembagian);
        System.out.println();
        System.out.println("=================================");

        System.out.println();
        System.out.println("Operasi Div");
        int hasilDiv= 5/2;
        System.out.println("Hasil 5 / 2 = " + hasilDiv);
        System.out.println();
        System.out.println("=================================");

        System.out.println();
        System.out.println("Operasi Mod");
        int hasilMod = 5%2;
        System.out.println("Hasil 5 % 2 = " + hasilMod);
        System.out.println();
        System.out.println("=================================");
    }
}
